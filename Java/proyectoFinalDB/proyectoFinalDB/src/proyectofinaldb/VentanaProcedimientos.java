    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        // TODO add your handling code here:
             DefaultTableModel model = (DefaultTableModel) tbl_entradas.getModel(); 
         //Sección 2
          int a = tbl_entradas.getSelectedRow(); 
         //Sección 3
          if (a<0){ 
                JOptionPane.showMessageDialog(null, 

                "Debe seleccionar una fila de la tabla, haz click sobre ella" ); 
         }else {          
           //Sección 4
            int confirmar=JOptionPane.showConfirmDialog(null, 

           "Esta seguro que desea Eliminar el registro? ");
            //Sección 5 
        if(JOptionPane.OK_OPTION==confirmar) {                  

                  //Sección 6
                   model.removeRow(a); 
                  //Sección 7
                   JOptionPane.showMessageDialog(null,"Registro Eliminado" );

 

            } 

 

        } 
    }//GEN-LAST:event_btn_eliminarActionPerformed

    private void btn_ejecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ejecutarActionPerformed
        // TODO add your handling code here:
           String texto= cb_tipo.getSelectedItem().toString();
           String name= txt_procedimiento.getText().trim().toString();
            if(texto.equals("Procedimiento")){
           DefaultTableModel modelo=(DefaultTableModel) tbl_entradas.getModel();
         
           
           
             DefaultTableModel rs= str_conn.Procedimientos(modelo,name);
            
             tbl_result.setModel(rs);
            }
            if(texto.equals("Funcion")){
            
           str_conn.funciones(name);
            }
    }//GEN-LAST:event_btn_ejecutarActionPerformed

