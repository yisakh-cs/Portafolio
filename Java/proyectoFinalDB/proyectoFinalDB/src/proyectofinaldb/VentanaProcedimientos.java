    private void cb_tipoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cb_tipoItemStateChanged
        // TODO add your handling code here:
        String texto= cb_tipo.getSelectedItem().toString();
       
        if(texto.equals("Funcion")){
        txt_nombreP.setEnabled(false);
        txt_valor.setEnabled(false);
        btn_eliminar.setEnabled(false);
        btn_agregar1.setEnabled(false);
        
        }
        if(texto.equals("Procedimiento")){
        
         txt_nombreP.setEnabled(true);
        txt_valor.setEnabled(true);
        btn_eliminar.setEnabled(true);
         btn_agregar1.setEnabled(true);
        }
    }//GEN-LAST:event_cb_tipoItemStateChanged

